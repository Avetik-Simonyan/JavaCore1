package chapter5;

public class StringSwitch {
    public static void main(String[] args) {
        String str = "hing";
        switch (str) {
            case "mek":
                System.out.println("mek");
                break;
            case "erku":
                System.out.println("erku");
                break;
            case "ereq":
                System.out.println("ereq");
                break;
            default:
                System.out.println("chhamapatasxanec");
                break;
        }
    }
}
